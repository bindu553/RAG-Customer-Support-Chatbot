# MAIN CHATBOT FUNCTION
# ============================================
def run_chatbot():
    """Main chatbot execution"""

    print("\n💡 Instructions:")
    print("   • Type your question naturally")
    print("   • Type 'human' to speak to a real person")
    print("   • Type 'status' to see system stats")
    print("   • Type 'exit' to quit\n")

    session_stats = {
        "total_queries": 0,
        "human_escalations": 0,
        "intents_detected": {}
    }

    while True:
        print("-"*50)
        user_input = input("👤 Customer: ").strip()

        if user_input.lower() in ['exit', 'quit', 'bye']:
            print("\n" + "="*50)
            print("📊 SESSION SUMMARY")
            print("="*50)
            print(f"📈 Total Queries: {session_stats['total_queries']}")
            print(f"👨‍💼 Human Escalations: {session_stats['human_escalations']}")
            if session_stats['total_queries'] > 0:
                resolution_rate = (session_stats['total_queries'] - session_stats['human_escalations']) / session_stats['total_queries'] * 100
                print(f"🤖 Bot Resolution Rate: {resolution_rate:.0f}%")
            print("\n👋 Thank you for contacting customer support!")
            break

        if not user_input:
            continue

        session_stats["total_queries"] += 1

        # Direct human request
        if user_input.lower() in ['human', 'agent', 'talk to human', 'real person']:
            print("\n🔄 Transferring to human support...")
            human_response = human_escalation(user_input, "User requested human", "Direct human request")
            print(f"\n👨‍💼 Agent: {human_response}\n")
            session_stats["human_escalations"] += 1
            continue

        # Status check
        if user_input.lower() == 'status':
            print(f"\n📊 System Status:")
            print(f"   • Queries processed: {session_stats['total_queries']}")
            print(f"   • Human handoffs: {session_stats['human_escalations']}")
            print(f"   • Active intents: {len(SUPPORT_KNOWLEDGE)}")
            print(f"   • RAG System: Active\n")
            continue

        # Step 1: Detect intent
        intent, confidence = detect_intent(user_input)
        session_stats["intents_detected"][intent] = session_stats["intents_detected"].get(intent, 0) + 1
        print(f"🎯 Intent: {intent} | Confidence: {int(confidence*100)}%")

        # Step 2: Retrieve answer
        answers = retrieve_answer(user_input, intent)
        print(f"📚 Retrieved {len(answers)} relevant answers")

        # Step 3: Generate response
        response = generate_response(user_input, intent, answers, confidence)

        # Step 4: Check if HITL needed
        if confidence < 0.5 or len(answers) == 0:
            print("⚠️ Low confidence - escalating to human...")
            response = human_escalation(user_input, response, f"Low confidence ({confidence:.0%})")
            session_stats["human_escalations"] += 1

        # Step 5: Output
        print(f"\n🤖 Assistant: {response}\n")

