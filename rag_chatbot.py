# RESPONSE GENERATION
# ============================================
def generate_response(question, intent, answers, confidence):
    """Generate friendly response"""

    templates = {
        "returns_refunds": "💰 I understand you need help with a return or refund. ",
        "shipping_delivery": "📦 Let me help you with shipping and delivery information. ",
        "account_management": "🔐 I can help with your account-related questions. ",
        "order_management": "📝 I'll assist you with your order. ",
        "warranty": "🛡️ Let me help you with warranty information. ",
        "contact_support": "💬 I'll help you connect with our support team. ",
        "payment_billing": "💳 Let me assist with payment and billing. ",
        "product_info": "📋 I can help with product information. ",
        "general": "🤝 Thank you for reaching out. "
    }

    prefix = templates.get(intent, templates["general"])

    if answers:
        main_answer = answers[0]
    else:
        main_answer = "I want to help, but I need more information. Could you please provide more details?"

    confidence_note = ""
    if confidence < 0.5:
        confidence_note = "\n\n⚠️ I'm not completely sure. Type 'human' to speak to an agent."
    elif confidence < 0.7:
        confidence_note = "\n\n📌 Type 'human' if this doesn't answer your question."

    return prefix + main_answer + confidence_note

