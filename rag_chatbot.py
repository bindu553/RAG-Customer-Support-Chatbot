# HUMAN-IN-THE-LOOP (HITL)
# ============================================
def human_escalation(user_question, bot_response, reason):
    """Handle HITL escalation"""
    print("\n" + "="*50)
    print("👨‍💼 HUMAN-IN-THE-LOOP ESCALATION")
    print("="*50)
    print(f"📋 Reason: {reason}")
    print(f"👤 Question: {user_question}")
    print(f"🤖 Bot said: {bot_response}")
    print("-"*50)
    print("📢 Notifying available agents...")

    human_response = input("👨‍💼 Human Agent Response: ")
    print("="*50)
    return human_response

