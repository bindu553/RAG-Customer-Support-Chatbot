# ============================================
# INTENT DETECTION
# ============================================
def detect_intent(question):
    """Detect customer intent from question"""
    q = question.lower()

    best_intent = "general"
    best_score = 0.0

    for intent, data in SUPPORT_KNOWLEDGE.items():
        score = 0
        keywords = data['keywords']

        for keyword in keywords:
            if keyword in q:
                score += 1

        if keywords:
            score = score / len(keywords)

        if score > best_score:
            best_score = score
            best_intent = intent

    if best_score > 0.5:
        best_score = min(best_score + 0.2, 1.0)

    return best_intent, best_score

print("✅ Intent detection ready")

